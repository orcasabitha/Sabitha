/* basic datatypes */

#include<stdio.h>


int main(void)
{

	int a = 100;
	float b = 331.79;
	char c = 'S';
	double d = 8.44e+11;


	printf("a = %i\n", a);
	printf("b = %f\n", b);
	printf("c = %c\n", c);
	printf("d = %e\n", d);
	printf("d = %g\n", d);


	return 0;

}


